package solid.srp;

/*
 * SRP- Single Responsibility Principle
 * A class should have one and only one reason to change, meaning that a class should have only one job.
 * it makes your software easier to implement and prevents unexpected side effects of future changes.
 * This reduces the number of bugs, improves your development speed
 *
 * SRP used following patterns
 * - Facade Pattern groups related functionality into a single interface, keeping each class focused on one responsibility.
 * - Observer Pattern decouples event handling, so notification logic is separate from business logic.
 *
 * {@link <a href="https://stackify.com/solid-design-principles/">...</a>}
 *
 */
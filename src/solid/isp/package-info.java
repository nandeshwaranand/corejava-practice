package solid.isp;

/*
 * ISP -  Interface Segregation Principle
 * A class should not be forced to implement methods it does not use.
 * Similar to the Single Responsibility Principle, the goal of the Interface Segregation Principle
 * is to reduce the side effects and frequency of required changes by splitting the software into multiple, independent parts.
 * ISP used following design patterns:
 * - Adapter Pattern allows a class to work with another class without modifying its interface.
 * - Interface Segregation ensures classes implement only the methods they need.
 *
 * https://stackify.com/interface-segregation-principle/
 */
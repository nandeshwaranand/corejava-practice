package solid.ocp;

/*
 * OCP - Open/Closed Principle
 * “Software entities (classes, modules, functions, etc.)
 * should be open for extension, but closed for modification.”
 *
 * It tells you to write your code so that you will be able to add new functionality without changing
 * the existing code.The main benefit of this approach is that an interface introduces an additional
 * level of abstraction which enables loose coupling.
 *
 * OCP used following design patterns:
 * - Strategy Pattern allows extending behavior dynamically without modifying existing code.
 * - Factory Pattern lets us create new objects without modifying existing logic
 * https://stackify.com/solid-design-open-closed-principle/
 */